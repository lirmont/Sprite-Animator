			reseneList.Add(new ReseneColor("Zumthor", 237, 246, 255));
			reseneList.Add(new ReseneColor("Zydeco", 2, 64, 44));
			#endregion
			// Put a copy of this stuff into the unique list.
			copyMainListToUniqueList();
		}

		private static void copyMainListToUniqueList()
		{
			reseneList.ForEach(delegate(ReseneColor c) {
				ReseneColor uniqueColor = uniqueReseneList.Find(item => item.r == c.r && item.g == c.g && item.b == c.b);
				if (uniqueColor == null)
					uniqueReseneList.Add(c);
			});
		}

		public class ReseneColor
		{
			public string name = "(no name)";
			public int r, g, b;

			public ReseneColor(string name, int r, int g, int b)
			{
				this.name = name;
				this.r = r;
				this.g = g;
				this.b = b;
			}

			public ReseneColor(string code, string name)
			{
				this.name = name;
				int cr = 0, cg = 0, cb = 0;
				cr = Convert.ToInt32(code.Substring(0, 2), 16);
				cg = Convert.ToInt32(code.Substring(2, 2), 16);
				cb = Convert.ToInt32(code.Substring(4, 2), 16);
				this.r = cr;
				this.g = cg;
				this.b = cb;
			}

			public static implicit operator System.Drawing.Color(ReseneColor c)
			{
				return System.Drawing.Color.FromArgb(255, c.r, c.g, c.b);
			}

			public override string ToString()
			{
				return string.Format("ReseneColor({0}, {1}, {2}, {3})", new object[]{ name, r, g, b });
			}
		}
